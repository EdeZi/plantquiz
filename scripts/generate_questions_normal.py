CATEGORIES = sorted({species.category for species in SPECIES_DATA})
PRODUCTS = sorted({species.product for species in SPECIES_DATA})
ORIGINS = sorted({species.origin for species in SPECIES_DATA})
FAMILIES = sorted({species.family for species in SPECIES_DATA})
COMMON_NAMES = [species.common for species in SPECIES_DATA]
LATIN_NAMES = [species.latin for species in SPECIES_DATA]
