SPECIES_DATA = SPECIES_DATA[:120]


def sample_distractors(pool: Sequence[str], correct: str, k: int) -> List[str]:
    options = [item for item in pool if item != correct]
    if len(options) < k:
        raise ValueError(f"Not enough distractors to sample {k} unique options from {len(set(pool))} items")
    return RNG.sample(options, k)


def build_choices(correct: str, wrong: Iterable[str]) -> List[dict]:
    options = [correct, *wrong]
    RNG.shuffle(options)
    choices = []
    for idx, text in enumerate(options):
        entry = {"id": chr(ord("a") + idx), "text": text}
        if text == correct:
            entry["correct"] = True
        choices.append(entry)
    return choices


LEVEL_COUNTERS = {level: 1 for level in range(1, 13)}
QUESTIONS: List[dict] = []


def add_question(level: int, prompt: str, correct: str, wrong: Sequence[str], explanation: str) -> None:
    question_id = f"L{level:02d}Q{LEVEL_COUNTERS[level]:03d}"
    LEVEL_COUNTERS[level] += 1
    QUESTIONS.append(
        {
            "id": question_id,
            "level": level,
            "prompt": prompt,
            "choices": build_choices(correct, wrong),
            "explanation": explanation,
        }
    )


